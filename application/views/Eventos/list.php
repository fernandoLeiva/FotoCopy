    <script>
  $(function () {

    /* initialize the external events
     -----------------------------------------------------------------*/
    function init_events(ele) {
      ele.each(function () {

        // create an Event Object (http://arshaw.com/fullcalendar/docs/event_data/Event_Object/)
        // it doesn't need to have a start or end
        var eventObject = {
          title: $.trim($(this).text()) // use the element's text as the event title
        }

        // store the Event Object in the DOM element so we can get to it later
        $(this).data('eventObject', eventObject)

        // make the event draggable using jQuery UI
        $(this).draggable({
          zIndex        : 1070,
          revert        : true, // will cause the event to go back to its
          revertDuration: 0  //  original position after the drag
        })

      })
    }

    init_events($('#external-events div.external-event'))

    /* initialize the calendar
     -----------------------------------------------------------------*/

    //Date for the calendar events (dummy data)
    var date = new Date()
    var d    = date.getDate(),
        m    = date.getMonth(),
        y    = date.getFullYear()

    $('#calendar').fullCalendar({
      header    : {
        left  : 'prev,next today',
        center: 'title',
        right : 'month,agendaWeek,agendaDay'
      },
      buttonText: {
        today: 'today',
        month: 'month',
        week : 'week',
        day  : 'day'
      },
      //Random default events
      events: function (start, end, timezone, callback) {
        $.ajax({
            type:'GET',
            url: 'Evento/Obtener_eventos',
            dataType: 'json',
            data:'',
            error: function (xhr, type, exception) { alert("Error: " + exception); },
            success: function (result) {
                callback(result);
            }
        });  
      },
      editable  : true,
      droppable : true, // this allows things to be dropped onto the calendar !!!
      eventResize: function(event, delta, revertFunc) {
           $.ajax({
            type:'POST',
            url: 'Evento/Modificar_evento',
            dataType: 'json',
            data:{"id": event.id, "fin": event.end.format()},
            success:function(result){
              alert("Evento Actualizado...");
            },
            error: function(result){
              alert("Error...");
            }
        }); 

      }, 
      eventDrop: function(event, delta, revertFunc) {
        var end='';
        if(event.end!=null){
          end= event.end.format();
        }else{
          end= event.start.format();
        }

       $.ajax({
          method: 'POST',
          dataType: 'json',
          url:'Evento/Transladar_evento',
          data:{"id": event.id, "inicio": event.start.format(), "fin": end},
          success:function(result){
            alert("Evento Actualizado...");
          },
          error: function(result){
            alert("Error...");
          }

        });  
      },
      
      eventDragStop: function(event,jsEvent) {
        event.visible = false;
        $('#calendar').fullCalendar('updateEvent', event);
        var trashEl = $('#calendarTrash');
        var ofs = trashEl.offset();

        var x1 = ofs.left;
        var x2 = ofs.left + trashEl.outerWidth(true);
        var y1 = ofs.top;
        var y2 = ofs.top + trashEl.outerHeight(true);

        if (jsEvent.pageX >= x1 && jsEvent.pageX<= x2 &&
            jsEvent.pageY >= y1 && jsEvent.pageY <= y2) {
          $.ajax({
            method: 'POST',
            dataType: 'json',
            url:'Evento/Eliminar_evento',
            data:{"id": event.id},
            success:function(result){
             alert("Evento Eliminado...");
            },
            error: function(result){
              alert("Error...");
            }
        }); 
            $('#calendar').fullCalendar('removeEvents', event.id);
        }
      },
      drop: function (date, allDay) { // this function is called when something is dropped

        // retrieve the dropped element's stored Event Object
        var originalEventObject = $(this).data('eventObject')

        // we need to copy it, so that multiple events don't have a reference to the same object
        var copiedEventObject = $.extend({}, originalEventObject)

            $.ajax({
                method: 'POST',
                dataType: 'json',
                url:'Evento/Guardar_evento',
                data:{"nombre": originalEventObject.title, "inicio": date.format(), "fin": date.format(), "color":$(this).css('background-color') },
                success:function(result){
                  alert("Evento Guardado...");
                  ActualizarPagina();
                  
                },
                error: function(result){
                  alert("Error...");
                }

             });

        // assign it the date that was reported
        copiedEventObject.start           = date
        copiedEventObject.allDay          = allDay
        copiedEventObject.backgroundColor = $(this).css('background-color')
        copiedEventObject.borderColor     = $(this).css('border-color')

        // render the event on the calendar
        // the last `true` argument determines if the event "sticks" (http://arshaw.com/fullcalendar/docs/event_rendering/renderEvent/)
        $('#calendar').fullCalendar('renderEvent', copiedEventObject, true)

        // is the "remove after drop" checkbox checked?
        if ($('#drop-remove').is(':checked')) {
          // if so, remove the element from the "Draggable Events" list
          $(this).remove()
        }

      }
    })
    

    /* ADDING EVENTS */
    var currColor = '#3c8dbc' //Red by default
    //Color chooser button
    var colorChooser = $('#color-chooser-btn')
    $('#color-chooser > li > a').click(function (e) {
      e.preventDefault()
      //Save color
      currColor = $(this).css('color')
      //Add color effect to button
      $('#add-new-event').css({ 'background-color': currColor, 'border-color': currColor })
    })
    $('#add-new-event').click(function (e) {
      e.preventDefault()
      //Get value and make sure it is not null
      var val = $('#new-event').val()
      if (val.length == 0) {
        return
      }

      //Create events
      var event = $('<div />')
      event.css({
        'background-color': currColor,
        'border-color'    : currColor,
        'color'           : '#fff'
      }).addClass('external-event')
      event.html(val)
      $('#external-events').prepend(event)

      //Add draggable funtionality
      init_events(event)

      //Remove event from text input
      $('#new-event').val('')
    })
  })

function ActualizarPagina(){ //Funcion Resfresca
  $('#content').empty();
  $("#content").load("<?php echo base_url(); ?>index.php/Evento/index/<?php echo $permission; ?>");
}
</script>